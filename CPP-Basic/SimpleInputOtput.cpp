#include<iostream>
using namespace std;
int main()
{
    int num1, num2;

    cout<<"Enter First Number: ";
    cin>>num1;
    cout<<"Enter Second Number: ";
    cin>>num2;
    /*
    "<<" = Insertion Operator / Printing operator
    ">>" = Extraction Operator / Scanning Operator
    */

    cout<<"The Sum is: "<<num1+num2;

    return 0;
}
